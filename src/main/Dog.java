public class Dog {
    
    private String colourOfCoat;
    private String breed;
    private String size;
    private String ownerName;
    private String foodType;
    
    private char gender;
    
    private int noOfLegs;
    private int age;
    
    private double weight;
    private double height;
    private double distanceWalked;
    
    private boolean trained;
    private boolean isHungry;
    private boolean teethDirty;
    private boolean isHappy;
    
    public Dog() {
        
    }
    
    public void setColourOfCoat(String colourOfCoat) {
        this.colourOfCoat = colourOfCoat;
    }
    
    public String getColourOfCoat() {
        return colourOfCoat;
    }
    
    public void setBreed(String breed) {
        this.breed = breed;
    }
    
    public String getBreed() {
        return breed;
    }
    
    public void setSize(String size) {
        this.size = size;
    }
    
    public String getSize() {
        return size;
    }
    
    public void setOwnerName(String ownerName) {
        this.ownerName = ownerName;
    }
    
    public String getOwnerName() {
        return ownerName;
    }
    
    public void setFoodType(String foodType) {
        this.foodType = foodType;
    }
    
    public String getFoodType() {
        return foodType;
    }
    
    public void setGender(char gender) {
        this.gender = gender;
    }
    
    public char getGender() {
        return gender;
    }
    
    public void setNoOfLegs(int noOfLegs) {
        this.noOfLegs = noOfLegs;
    }
    
    public int getNoOfLegs() {
        return noOfLegs;
    }
    
    public void setAge(int age) {
        this.age = age;
    }
    
    public int getAge() {
        return age;
    }
    
    public void setWeight(double weight) {
        this.weight = weight;
    }
    
    public double getWeight() {
        return weight;
    }
    
    public void setHeight(double height) {
        this.height = height;
    }
    
    public double getHeight() {
        return height;
    }
    
    public void setDistanceWalked(double distanceWalked) {
        this.distanceWalked = distanceWalked;
    }
    
    public double getDistanceWalked() {
        return distanceWalked;
    }
    
    public void setTrained(boolean trained) {
        this.trained = trained;
    }
    
    public boolean getTrained() {
        return trained;
    }
    
    public void setIsHungry(boolean isHungry) {
        this.isHungry = isHungry;
    }
    
    public boolean getIsHungry() {
        return isHungry;
    }
    
    public void setTeethDirty(boolean teethDirty) {
        this.teethDirty = teethDirty;
    }
    
    public boolean getTeethDirty() {
        return teethDirty;
    }
    
    public void setIsHappy(boolean isHappy) {
        this.isHappy = isHappy;
    }
    
    public boolean getIsHappy() {
        return isHappy;
    }
}
